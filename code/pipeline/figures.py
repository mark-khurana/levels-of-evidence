def _tier(n):
    if n >= 1.0: return 'A'
    if n >= 0.75: return 'B'
    if n >= 0.5: return 'C'
    if n >= 0.25: return 'D'
    return 'E'


def _pct_sum100(counts, total):
    """One-decimal percentages that sum to exactly 100.0 (largest-remainder)."""
    tenths = [c / total * 1000 for c in counts]
    floor = [int(x) for x in tenths]
    need = round(sum(tenths)) - sum(floor)
    for i in sorted(range(len(tenths)), key=lambda k: tenths[k] - floor[k], reverse=True)[:need]:
        floor[i] += 1
    return [f / 10 for f in floor]


def _textc(bg):
    return '#fff' if bg in ('#1b4f72', '#2980b9', '#c0392b') else '#333'


def normalize_loe(loe, qual, society):
    if society in LOE_EXCLUDED:
        return None
    for val in [loe, qual]:
        if not val or str(val) in ('Not stated', 'None', '', 'nan', 'N/A'):
            continue
        val = str(val).strip()
        if val in SKIP_STRENGTH:
            continue
        if society == 'ESC' and val in ESC_NORM:
            return ESC_NORM[val]
        if val in NORM:
            return NORM[val]
    return None


def load_data():
    """Load from evidence_atlas.json (single source of truth, matches manuscript)."""
    atlas_path = DATA_DIR / "evidence_atlas.json"
    with open(atlas_path) as f:
        atlas = json.load(f)

    gl_map = {g['id']: g for g in atlas['guidelines']}

    recs_by_spec = defaultdict(list)
    recs_by_soc = defaultdict(list)
    soc_gl_count = Counter()
    spec_gl_count = Counter()
    soc_grading = defaultdict(Counter)

    for g in atlas['guidelines']:
        soc_gl_count[g['society']] += 1
        spec_gl_count[g['specialty']] += 1
        soc_grading[g['society']][g.get('grading_system', '?')] += 1

    for r in atlas['recommendations']:
        score = r.get('loe_normalized')
        if score is None:
            continue
        g = gl_map.get(r['guideline_id'], {})
        loe_label = r.get('loe', '')
        rec = {'score': score, 'tier': _tier(score), 'loe': loe_label,
               'society': r.get('society', g.get('society', '?')),
               'specialty': r.get('specialty', g.get('specialty', '?')),
               'guideline_id': r['guideline_id'],
               'grading': g.get('grading_system', '?'),
               'in_primary': bool(g.get('in_primary_analysis'))}
        recs_by_spec[rec['specialty']].append(rec)
        recs_by_soc[rec['society']].append(rec)

    return recs_by_spec, recs_by_soc, soc_gl_count, spec_gl_count, soc_grading


def primary_filter(recs_by_spec, spec_gl_count):
    """Filter to specialties with >=5 guidelines, using the canonical
    in_primary_analysis set (LoE-only societies, >=3 normalizable recs per
    guideline) so figure sample sizes match Table 1 / analysis.py."""
    return {s: [r for r in v if r.get('in_primary')]
            for s, v in recs_by_spec.items()
            if spec_gl_count.get(s, 0) >= 5
            and any(r.get('in_primary') for r in v)}


