# eFigure 1 — Sensitivity ranking
# ─────────────────────────────────────────────────
def efigure1(primary_specs):
    specs = {s: [r['score'] for r in v] for s, v in primary_specs.items()}

    scenarios = OrderedDict([('Conservative\n(-0.25)', -0.25), ('Base', 0), ('Generous\n(+0.25)', 0.25)])
    rankings = {}
    for lab, delta in scenarios.items():
        gaps = {s: 1 - statistics.mean([max(0.125, min(1.0, n + delta)) for n in norms]) for s, norms in specs.items()}
        rankings[lab] = sorted(gaps, key=gaps.get, reverse=True)

    fig, ax = plt.subplots(figsize=(6, max(4, 0.25 * len(specs) + 1)))
    slabs = list(scenarios.keys())
    ns = len(specs)
    for sp in specs:
        pos = [rankings[sc].index(sp) for sc in slabs]
        mx = max(abs(p - pos[1]) for p in pos)
        col = '#1b4f72' if mx == 0 else '#85c1e9' if mx <= 1 else '#c0392b'
        ax.plot(range(3), pos, 'o-', color=col, alpha=0.8, ms=5, lw=1.5 if mx == 0 else 1.2)
        ax.text(2.15, pos[-1], SHORT_SPECS.get(sp, sp), fontsize=6, va='center', color=col,
                fontweight='bold' if mx == 0 else 'normal')

    ax.set_xticks(range(3))
    ax.set_xticklabels(slabs, fontsize=7.5)
    ax.set_ylabel('Rank (1 = largest gap)', fontsize=8)
    ax.set_ylim(ns - 0.5, -0.5)
    ax.set_yticks(range(ns))
    ax.set_yticklabels([str(i + 1) for i in range(ns)], fontsize=7)
    ax.spines['top'].set_visible(False)
    ax.spines['right'].set_visible(False)
    ax.set_xlim(-0.2, 3.5)
    ps = [mpatches.Patch(color='#1b4f72', label='Stable'),
          mpatches.Patch(color='#85c1e9', label='Minor (1 pos)'),
          mpatches.Patch(color='#c0392b', label='Major (2+ pos)')]
    ax.legend(handles=ps, fontsize=6, frameon=True, facecolor='white', edgecolor='#ddd', loc='lower left')

    fig.tight_layout()
    for fmt in ('pdf', 'png'):
        fig.savefig(FIG_DIR / f'efigure2_sensitivity_ranking.{fmt}', dpi=300, bbox_inches='tight')
    plt.close(fig)
    print('eFigure 2 (sensitivity ranking) saved')


# ─────────────────────────────────────────────────
# eFigure 2 — Access coverage by society
# ─────────────────────────────────────────────────
def efigure2(soc_gl_count):
    cov = []
    for soc in sorted(soc_gl_count.keys()):
        ext = soc_gl_count[soc]
        if soc in FULLY_COMPLETE:
            tot = ext
        else:
            tot = max(AUDIT_DENOM.get(soc, ext), ext)
        cov.append((soc, tot, ext))

    cov.sort(key=lambda x: -x[1])

    fig, ax = plt.subplots(figsize=(6.5, max(5.5, 0.28 * len(cov) + 1)))
    for yi, (soc, tot, ext) in enumerate(cov):
        ax.barh(yi, ext, height=0.55, color='#2980b9', edgecolor='white', lw=0.3)
        ax.barh(yi, tot - ext, left=ext, height=0.55, color='#e5e5e5', edgecolor='white', lw=0.3)
        if ext > 0:
            ax.text(ext / 2, yi, str(ext), ha='center', va='center', fontsize=6, color='white', fontweight='bold')
        ax.text(tot + 0.8, yi, str(tot), ha='left', va='center', fontsize=6, color='#999')

    ax.set_yticks(range(len(cov)))
    ax.set_yticklabels([s for s, _, _ in cov], fontsize=7)
    ax.set_xlabel('Number of Guidelines', fontsize=8)
    ax.invert_yaxis()
    ax.spines['top'].set_visible(False)
    ax.spines['right'].set_visible(False)
    tot_all = sum(t for _, t, _ in cov)
    tot_ext = sum(e for _, _, e in cov)
    ps = [mpatches.Patch(color='#2980b9', label=f'Extracted ({tot_ext}, {tot_ext / tot_all * 100:.0f}%)'),
          mpatches.Patch(color='#e5e5e5', label=f'Not accessible ({tot_all - tot_ext})')]
    ax.legend(handles=ps, fontsize=7, frameon=True, facecolor='white', edgecolor='#ddd', loc='lower right')

    fig.tight_layout()
    for fmt in ('pdf', 'png'):
        fig.savefig(FIG_DIR / f'efigure2_access_coverage.{fmt}', dpi=300, bbox_inches='tight')
    plt.close(fig)
    print('eFigure 2 saved')


