# Figure 1 — Specialty evidence distribution
# ─────────────────────────────────────────────────
def figure1(specs):
    specs = dict(specs)  # copy
    order = sorted(specs, key=lambda s: statistics.mean([r['score'] for r in specs[s]]))

    # Pooled distribution across all primary specialties -> "Overall" summary row
    all_recs = [r for sp in order for r in specs[sp]]
    # Rows top-to-bottom: Overall, then specialties (largest evidence gap first)
    rows = [('Overall', all_recs)] + [(SHORT_SPECS.get(s, s), specs[s]) for s in order]

    n_rows = len(rows)
    # JAMA: trimmed content lands at ~7 in wide (full width) and ~9 in tall
    # (≈ a portrait page) at 300 dpi; canvas is larger because bbox_inches
    # trims ~30% of the width.
    fig_h = max(5, 0.34 * n_rows + 1.3)
    fig, ax1 = plt.subplots(figsize=(10.5, fig_h))
    bh = 0.66

    # x positions for the exact-value columns to the right of the bars
    COL_X = {'A': 108, 'B': 118, 'C': 128, 'D': 138, 'E': 148}
    GAP_X = 160

    for yi, (label, recs) in enumerate(rows):
        total = len(recs)
        tc = Counter(r['tier'] for r in recs)
        # largest-remainder rounding so the printed %s and bar widths sum to 100.0
        pcts = _pct_sum100([tc.get(t, 0) for t in C], total)
        left = 0
        for t, pct in zip(C, pcts):
            if pct > 0:
                ax1.barh(yi, pct, left=left, height=bh, color=C[t], edgecolor='white', lw=0.3)
                left += pct
            # exact value for every tier, in an aligned column (readable even for <1% tiers)
            ax1.text(COL_X[t], yi, f'{pct:.1f}', ha='center', va='center', fontsize=8.5, color='#333')
        gap = 1 - statistics.mean([r['score'] for r in recs])
        ax1.text(GAP_X, yi, f'{gap:.2f}', ha='center', va='center', fontsize=9, fontweight='bold', color='#333')

    def _row_label(label, recs):
        n_soc = len({r['society'] for r in recs})
        n_gl = len({r['guideline_id'] for r in recs})
        return f'{label}  ({n_soc} soc, {n_gl} GL, n={len(recs):,})'

    ax1.set_yticks(range(n_rows))
    ax1.set_yticklabels([_row_label(label, recs) for label, recs in rows], fontsize=8.5)
    # Emphasize the Overall summary row
    ax1.get_yticklabels()[0].set_fontweight('bold')
    ax1.set_xlim(0, 100)
    ax1.invert_yaxis()
    # Trim the empty band above the header row and below the last bar
    ax1.set_ylim(n_rows - 0.5, -0.95)
    ax1.spines['top'].set_visible(False)
    ax1.spines['right'].set_visible(False)
    ax1.set_xlabel('% of Recommendations', fontsize=8.5)
    # Column headers for the exact-value table (% at each tier, then gap)
    for t, x in COL_X.items():
        ax1.text(x, -0.62, f'%{t}', ha='center', fontsize=9, fontweight='bold', color='#666')
    ax1.text(GAP_X, -0.62, 'Gap', ha='center', fontsize=9, fontweight='bold', color='#666')
    patches = [mpatches.Patch(color=c, label=C_LABELS[t]) for t, c in C.items()]
    ax1.legend(handles=patches, loc='upper center', bbox_to_anchor=(0.5, -0.075),
               fontsize=7.5, frameon=False, ncol=3, columnspacing=1.2, handlelength=1.2)

    fig.tight_layout()
    for fmt in ('pdf', 'png'):
        fig.savefig(FIG_DIR / f'figure2_evidence_distribution.{fmt}', dpi=300, bbox_inches='tight', pad_inches=0.03)
    plt.close(fig)
    print('Figure 1 saved')


